/**
 * Config controller class.
 *
 * @package    local_xp
 * @copyright  2017 Frédéric Massart
 * @author     Frédéric Massart <fred@branchup.tech>
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class config_controller extends \block_xp\local\controller\config_controller {

    /**
     * Define the form.
     *
     * @param bool $withblockconfig With block config?
     * @return moodleform
     */
    protected function define_form($withblockconfig = false) {
        return new \local_xp\form\config($this->pageurl->out(false), [
            'showblockconfig' => $withblockconfig,
            'world' => $this->world,
        ]);
    }

}
